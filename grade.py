def cunygrade(grade):
    if grade == "A+":  # A+
        return 4.0
    elif grade == "A":  # A
        return 4.0
    elif grade == "A-":  # A-
        return 3.7
    elif grade == "B+":  # B+
        return 3.3
    elif grade == "B":  # B
        return 3.0
    elif grade == "B-":  # B-
        return 2.7
    elif grade == "C+":  # C+
        return 2.3
    elif grade == "C":  # C
        return 2.0
    elif grade == "C-":  # C-
        return 1.7
    elif grade == "D+":  # D+
        return 1.3
    elif grade == "D":  # D
        return 1.0
    elif grade == "F":  # F
        return 0